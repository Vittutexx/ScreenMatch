public class Animal {
    private String andar;
    private String comer;


    public Animal(String andar, String comer) {
        this.andar = andar;
        this.comer = comer;
    }

    public String getAndar() {
        return andar;
    }

    public void setAndar(String andar) {
        this.andar = andar;
    }

    public String getComer() {
        return comer;
    }

    public void setComer(String comer) {
        this.comer = comer;
    }
}
